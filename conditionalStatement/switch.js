let day='Friday';
switch (day) {
    case 'Monday':
        console.log('Week of first day');
        break;
     case 'Tuesday':
        console.log('Week of first day');
        break;
     case 'Wednesday':
        console.log('Week of second day');
        break;
     case 'Thrusday':
        console.log('Week of third day');
        break;
     case 'Friday':
        console.log('Week of fourth day');
        break;
        
    case 'Saturday':
        console.log('Week of half day');
        break;
        
    case 'Sunday':
        console.log('Week of leave day');
        break;
        
    default:
        console.log('Please Enter valid day');
        break;
}